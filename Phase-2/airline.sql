CREATE TABLE FLIGHTS
(
  DESTINATION VARCHAR(3) NOT NULL,
  ORIGIN VARCHAR(3) NOT NULL,
  FLIGHT_NO VARCHAR(10) NOT NULL,
  DEP_TIME TIME NOT NULL,
  ARV_TIME TIME NOT NULL,
  PRIMARY KEY (FLIGHT_NO)
);

CREATE TABLE AIRPLANE
(
  MODEL VARCHAR(10) NOT NULL,
  SERIAL_NO INT NOT NULL,
  AGE INT,
  BUSINESS INT,
  ECONOMY INT,
  PRIMARY KEY (SERIAL_NO)
);

CREATE TABLE BOOKINGS
(
  PNR VARCHAR(10) NOT NULL,
  BOOKING_DATE TIMESTAMP NOT NULL,
  DATE_OF_DEPT TIMESTAMP NOT NULL,
  FLIGHT_NO VARCHAR(10)NOT NULL ,
  FARE INT NOT NULL,
  PRIMARY KEY (PNR),
  CONSTRAINT FK_FLI FOREIGN KEY(FLIGHT_NO) REFERENCES FLIGHTS(FLIGHT_NO)
);

CREATE TABLE CUSTOMER
(
  NAME VARCHAR(50) NOT NULL,
  AGE INT NOT NULL,
  GENDER CHAR(1) NOT NULL,
  CUSTOMER_ID INT NOT NULL,
  PRIMARY KEY (CUSTOMER_ID)
);

CREATE TABLE EMPLOYEE
(
  EMP_ID INT NOT NULL,
  DESIGNATION VARCHAR(20) NOT NULL,
  SALARY INT NOT NULL,
  NAME VARCHAR(50) NOT NULL,
  PRIMARY KEY (EMP_ID)
);

CREATE TABLE AIRPORT
(  
  NAME VARCHAR(40) NOT NULL,
  CODE VARCHAR(3) NOT NULL,
  PRIMARY KEY (CODE)
);

CREATE TABLE FOOD
(
  SKU INT NOT NULL,
  NAME VARCHAR(30) NOT NULL,
  COST INT NOT NULL,
  QUANTITY INT NOT NULL,
  PRIMARY KEY (SKU)
);

CREATE TABLE FLIES(
  FLIGHT_NO VARCHAR(10) NOT NULL,
  SERIAL_NO INTEGER NOT NULL,
  CONSTRAINT FK_FLI FOREIGN KEY(FLIGHT_NO) REFERENCES FLIGHTS(FLIGHT_NO),
  CONSTRAINT FK_AIR FOREIGN KEY(SERIAL_NO) REFERENCES AIRPLANE(SERIAL_NO));

CREATE TABLE HAS_BOOKING(
  FLIGHT_NO VARCHAR(10) NOT NULL,
  PNR VARCHAR(10) NOT NULL,
  CONSTRAINT FK_FLI FOREIGN KEY(FLIGHT_NO) REFERENCES FLIGHTS(FLIGHT_NO),
  CONSTRAINT FK_PNR FOREIGN KEY(PNR) REFERENCES BOOKINGS(PNR));

CREATE TABLE BOOKED_BY(
 PNR VARCHAR(10)  NOT NULL,
  CUSTOMER_ID INTEGER NOT NULL,
  CONSTRAINT FK_PNR FOREIGN KEY(PNR) REFERENCES BOOKINGS(PNR),
  CONSTRAINT FK_BKD FOREIGN KEY(CUSTOMER_ID) REFERENCES CUSTOMER(CUSTOMER_ID));

CREATE TABLE INCLUDES(
  SKU INTEGER NOT NULL,
  PNR VARCHAR(10)  NOT NULL,
  CONSTRAINT FK_SKU FOREIGN KEY(SKU) REFERENCES FOOD(SKU),
  CONSTRAINT FK_PNR FOREIGN KEY(PNR) REFERENCES BOOKINGS(PNR));

CREATE TABLE CREW(
  FLIGHT_NO VARCHAR(10) NOT NULL,
  EMP_ID INTEGER NOT NULL,
  CONSTRAINT FK_FLI FOREIGN KEY(FLIGHT_NO) REFERENCES FLIGHTS(FLIGHT_NO),
  CONSTRAINT FK_EMP FOREIGN KEY(EMP_ID) REFERENCES EMPLOYEE(EMP_ID));

CREATE TABLE WORKS_AT(
  AIRCODE VARCHAR(3) NOT NULL,
  EMP_ID INTEGER NOT NULL,
  CONSTRAINT FK_FLI FOREIGN KEY(AIRCODE) REFERENCES AIRPORT(CODE),
  CONSTRAINT FK_EMP FOREIGN KEY(EMP_ID) REFERENCES EMPLOYEE(EMP_ID));

